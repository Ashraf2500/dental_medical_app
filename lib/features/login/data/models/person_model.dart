
class Person {

  String name;
  String email;
  String password;
  String phone;
  String address;
  String birthday;
  int age;
  String gender;
  String occupation;
  String marital_status;

  Person({
   required this.name,
   required this.email,
   required this.password,
   required this.phone,
   required this.address,
   required this.birthday,
   required this.age,
   required this.gender,
   required this.occupation,
   required this.marital_status
  });
}